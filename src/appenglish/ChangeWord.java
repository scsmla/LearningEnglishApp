    private void searchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonMouseClicked
        try
        {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String sqlquery = "SELECT * FROM dbo.wordData WHERE word = '" + searchTextField.getText() + "'";
            connection = DriverManager.getConnection(connectionUrl);
            statement = connection.createStatement();
            resultSet =  statement.executeQuery(sqlquery);
            if(!resultSet.isBeforeFirst())
            {
                notifyLabel.setVisible(true);
                jLabel2.setVisible(false);
                changeMeaningRadioButton.setVisible(false);
                changeSubjectRadioButton.setVisible(false);
                ChangeImageRadioButton.setVisible(false);
                meaningTextField.setVisible(false);
                subjectComboBox.setVisible(false);
                chooseImageButton.setVisible(false);
                confirmChangeButton.setVisible(false);
            }
            else
            {
                notifyLabel.setVisible(false);
                jLabel2.setVisible(true);
                changeMeaningRadioButton.setVisible(true);
                changeSubjectRadioButton.setVisible(true);
                ChangeImageRadioButton.setVisible(true);
                meaningTextField.setVisible(true);
                subjectComboBox.setVisible(true);
                chooseImageButton.setVisible(true);
                confirmChangeButton.setVisible(true);
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_searchButtonMouseClicked

