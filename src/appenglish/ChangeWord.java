    private void confirmChangeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmChangeButtonMouseClicked
        String updateSql = "UPDATE dbo.wordData SET";
        if(changeMeaningRadioButton.isSelected())
        {
            updateSql += " meaning = N'"+ meaningTextField.getText() + "'";
        }
        if(changeSubjectRadioButton.isSelected())
        {
            updateSql += ",subjectOfWord = N'"+ subjectComboBox.getItemAt(subjectComboBox.getSelectedIndex()) + "' ";
        }
        if(ChangeImageRadioButton.isSelected())
        {
            updateSql += ",imageOfWord = N'" + pathToImage + "' ";
        }
        updateSql += "WHERE word = N'" + searchTextField.getText()+"'";
//        System.out.println(updateSql);
        try
        {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver"); 
            connection = DriverManager.getConnection(connectionUrl);  
            statement = connection.createStatement();
            prepsInsertProduct = connection.prepareStatement(  
            updateSql,  
            Statement.RETURN_GENERATED_KEYS);  
            prepsInsertProduct.execute();  

        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }    
        jLabel4.setVisible(true);
    }//GEN-LAST:event_confirmChangeButtonMouseClicked

